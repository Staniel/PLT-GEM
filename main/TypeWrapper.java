
public class TypeWrapper {
	Integer intVal = null;
	Double doubleVal = null;
	String strVal = null;
	Boolean boolVal = null;
	
	TypeWrapper() {
	}
	
	TypeWrapper(Integer i) {
		intVal = i;
	}
	
	TypeWrapper(Double f) {
		doubleVal = f;
	}
	
	TypeWrapper(String s) {
		strVal = s;
	}
	
	TypeWrapper(Boolean b) {
		boolVal = b;
	}
}
